// equal 
console.log(3 == 3); //true
console.log('3' == 3); //true bc of coersion

// strict equal check
console.log('3' === 3); //false 

// not equal
console.log(3 != 4);
console.log(3 !== '3'); //this returns true bc no coersion occurs

