//assignment operator (the single equal sign)
let r = 5;

//addition assignment operator (+=)
r += 1; // r = r + 1
console.log(r);

//subtraction assignment operator (-=)
r -= 1;
console.log(r);

//multiplication assignment operator (*=)
r *= 1;
console.log(r);

//divsion assignment operator
r /= 2;
console.log(r);

//remainder (modulus) assignment operator
r %= 2;
console.log(r);

// exponential assignment operator
r **= 1;
console.log(r);